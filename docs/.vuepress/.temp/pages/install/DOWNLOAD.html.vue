<h3 id="windows-instructions" tabindex="-1"><a class="header-anchor" href="#windows-instructions" aria-hidden="true">#</a> Windows Instructions</h3>
<p>Click the Windows link that matches your CPU architecture. Unzip using your
preferred too then launch using your preferred console. Murex cannot be
started via double clicking the executable -- it requires a starting from
within an existing console session.</p>
<p>Please also note that Windows support is also considered experimental. In part
due to the lack of <strong>coreutils</strong> (as seen on Linux and UNIX) and in part due to
the different underpinning technologies behind consoles / terminal emulators.
If you do experience some wonky behavior then our recommendation is to run the
<code v-pre>linux-amd64</code> build for Linux on top of WSL. The instructions above will guide
you through installing on Linux, WSL install instructions can be found at the
following site: <a href="https://docs.microsoft.com/en-us/windows/wsl/install-win10" target="_blank" rel="noopener noreferrer">docs.microsoft.com/en-us/windows/wsl/install-win10<ExternalLinkIcon/></a></p>
